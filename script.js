function random(){

    var alumesInterno = albumes;

    if(document.getElementById("genero").value !== "Todos"){
      alumesInterno = alumesInterno.filter(e => e["genero"] == document.getElementById("genero").value);
    }

    //generamos un numero aleatorio
    numero = Math.floor((Math.random()*100)%alumesInterno.length);

    //selecionamos el album
    album = alumesInterno[numero];

    //establecemos titulo
    document.getElementById("titulo").textContent = album["artista"] + " - " + album["nombre"];

    //establecemos portada
    document.getElementById("portada").src = album["portada"];
}

document.getElementById("random").addEventListener("click", random)